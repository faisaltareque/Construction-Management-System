    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String id=idTextField.getText();
        String name=nameTextField2.getText();
        String unitPrice=unitPriceTextField.getText();
        String estimatedAmount=estimatedAmountTextField.getText();
        String purchasedAmount=purchasedAmountTextField.getText();
        String totalCost=totalCostTextField.getText();

        try{
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection connection = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;databaseName=Construction;selectMethod=cursor", "sa", "123456");
            String query="insert into Material (ID,Name,UnitPrice,EstimatedAmount,PurchasedAmount,TotalCost)"
            +"values(?,?,?,?,?,?)";

            PreparedStatement pst=connection.prepareStatement(query);

            pst.setString(1,id);
            pst.setString(2,name);
            pst.setString(3,unitPrice);
            pst.setString(4,estimatedAmount);
            pst.setString(5,purchasedAmount);
            pst.setString(6,totalCost);

            pst.executeUpdate();
            JOptionPane.showMessageDialog(null,"Inserted Successfully!");

        }catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

