    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(flag){
            String name=nameTextField.getText();
            String unitPrice=unitPriceTextField.getText();
            String estimatedAmount=estimatedAmountTextField.getText();
            String purchasedAmount=purchasedAmountTextField.getText();
            String totalCost=totalCostTextField.getText();

        try{
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection connection = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;databaseName=Construction;selectMethod=cursor", "sa", "123456");
            String query="update Material set Name=?,UnitPrice=?,EstimatedAmount=?,PurchasedAmount=?,TotalCost=? where ID like ?";

            PreparedStatement pst=connection.prepareStatement(query);

            pst.setString(1,name);
            pst.setString(2,unitPrice);
            pst.setString(3,estimatedAmount);
            pst.setString(4,purchasedAmount);
            pst.setString(5,totalCost);
            pst.setString(6,id);


            pst.executeUpdate();
            JOptionPane.showMessageDialog(null,"Updated Successfully!");

        }catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        }
            
        }
    }//GEN-LAST:event_jButton2ActionPerformed

