    private void getComboBoxOption(){
        try{
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection connection = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;databaseName=Construction;selectMethod=cursor", "sa", "123456");
            String query="select ID from Material";         
            Statement statement = connection.createStatement();
            ResultSet resultSet = statement.executeQuery(query);
            while(resultSet.next()){
                String o=resultSet.getString("ID");
                jComboBox1.addItem(o);
            }
                    
        }catch (Exception e) {
          JOptionPane.showMessageDialog(null,"ID is not found!!!");
        }
    }
    
    private void LoadInfo(String id){
        Material material;
        try{
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection connection = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;databaseName=Construction;selectMethod=cursor", "sa", "123456");
            String query="select * from Material where ID like '"+id+"'";
            
            Statement statement = connection.createStatement();
            ResultSet resultSet = statement.executeQuery(query);            
            resultSet.next();              
                material=new Material(
                        resultSet.getString("ID"),
                        resultSet.getString("Name"),
                        resultSet.getString("UnitPrice"),
                        resultSet.getString("EstimatedAmount"),
                        resultSet.getString("PurchasedAmount"),
                        resultSet.getString("TotalCost"));
                               
            
            nameTextField.setText(material.getName());
            unitPriceTextField.setText(material.getUnitPrice());
            estimatedAmountTextField.setText(material.getEstimatedAmount());
            purchasedAmountTextField.setText(material.getPurchasedAmount());
            totalCostTextField.setText(material.getTotalCost());
            
        }catch (Exception e) {
          JOptionPane.showMessageDialog(null,e);
        }
    }

